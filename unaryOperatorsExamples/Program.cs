using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace unaryOperatorsExamples
{
    class Program
    {
        static void Main(string[] args)
        {
            int a = 0, b = 5;
            a++; // a = 1   
            b--; // b = 4

            Console.WriteLine(a + " " + b);
            Console.ReadKey();
        }
    }
}